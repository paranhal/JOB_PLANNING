// IsKRHoliday 해당 연·월·일이 공휴일이면 true (토일은 호출 전에 제외하는 것이 일반적).
func IsKRHoliday(y int, t time.Time) bool {
	if t.Weekday() == time.Saturday || t.Weekday() == time.Sunday {
		return true
	}
	key := t.Format("2006-01-02")
	if m, ok := krHolidayKeys[y]; ok {
		_, hit := m[key]
		return hit
	}
	return false
}
