func (r *MaintenanceRepo) InsertVisit(planID, visitDate, customerID string, sortOrder, autoGen int, entryCategory, notes string) error {
	id := randomID("mvs_")
	_, err := r.db.Exec(`
		INSERT INTO maintenance_visits
		(visit_id, plan_id, visit_date, customer_id, sort_order, auto_generated, entry_category, notes)
		VALUES (?,?,?,?,?,?,?,?)`,
		id, planID, visitDate, customerID, sortOrder, autoGen, entryCategory, nullIfEmpty(notes))
	return err
}

